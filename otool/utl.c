#include "otool.h"

#include "libft.h"

void		hex_dump(unsigned char hex)
{
	if (hex < 16)
		ft_printf("0%c ", ("0123456789abcdef")[hex % 16]);
	else
		ft_printf("%c%c ", ("0123456789abcdef")[hex / 16 % 16],
		("0123456789abcdef")[hex % 16]);
}
